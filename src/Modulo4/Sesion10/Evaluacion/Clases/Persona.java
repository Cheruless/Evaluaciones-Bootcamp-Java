package Modulo4.Sesion10.Evaluacion.Clases;

public abstract class Persona implements IMiembroEquipo {
    private String nombre;
    private String apellido;
    private int edad;

    public Persona(String nombre, String apellido, int edad) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.edad = edad;
    }

    public Persona() {
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    @Override
    public abstract String hablar();

    @Override
    public abstract String celebrar();

    @Override
    public abstract String amonestacionAmarilla();

    @Override
    public abstract String amonestacionRoja();
}
